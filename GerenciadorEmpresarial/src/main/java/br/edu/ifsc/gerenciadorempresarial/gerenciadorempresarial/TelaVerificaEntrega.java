    public void carregaCompras(){
        try{
            compras = ComprasSolicitadas.buscaCompras();
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Erro ao carregar Compras!");
            this.dispose();
        }
    }
    
    public void pesquisaEntrega(){
        codCompra = Integer.parseInt(edtCodigo.getText());
        if(compras.get(codCompra).getDataEntregue() == null){
            JOptionPane.showMessageDialog(this, "Sua mercadoria não foi entregue!");
        }else{
            JOptionPane.showMessageDialog(this, "Mercadoria entregue! Favor comparecer ao setor de almoxarifado.");
        }
    }



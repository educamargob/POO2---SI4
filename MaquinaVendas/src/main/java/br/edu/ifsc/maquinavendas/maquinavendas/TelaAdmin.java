    private void btnRelVendaIndivActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRelVendaIndivActionPerformed
        String arqRel = "E:\\Relatorios/";
        arqRel += "vendas_individual.jasper";
        try {
            DBController db = new DBController();
            JasperReport rel = (JasperReport) 
                    JRLoader.loadObjectFromFile(arqRel);
            
            db.conectar();
            JasperPrint imp = JasperFillManager.fillReport(rel, 
                                                        null, db.getConexao());
            db.desconectar();
            
            JasperViewer v = new JasperViewer(imp);
            v.setTitle("Relat√≥rio de Vendas Individual");
            v.setVisible(true);            
        } catch (JRException ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnRelVendaIndivActionPerformed

